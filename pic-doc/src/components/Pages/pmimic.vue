<h1 id="pmimic-critical-care-database">PIC (Pediatric Intensive Care) Database</h1>

<p>PIC (<strong>P</strong>ediatric <strong>I</strong>ntensive  <strong>C</strong>are) is a large, freely-available database comprising deidentified health-related data associated with over ten thousand pediatric patients who stayed in critical care units of the Children's Hospital, Zhejiang University School of Medicine between 2010 and 2019.</p>

<p>The database includes information such as demographics, vital sign measurements made during the surgery, laboratory test results, symptoms, medications, and mortality.</p>

<p>PIC supports a diverse range of analytic studies spanning epidemiology, clinical decision-rule improvement, and electronic tool development. It is notable for four factors:</p>

<ul>
<li>it is freely available to researchers worldwide</li>
<li>it encompasses a diverse and very large population of ICU patients</li>
<li>it contains high temporal resolution data including lab results, symptoms, and surgery monitor trends</li>
<li>it contains pediatric data not founded in MIMIC</li>
</ul>

